    format = d3.format(",d"),
    color = d3.scale.category20();

var bubble = d3.layout.pack()
    .sort(null)
    .size([diameter, diameter])
    .padding(20);

function getRandomInt(min, max) {
return Math.floor(Math.random() * (max - min + 1)) + min;
}

var svg = d3.select("#map").append("svg")
    .attr("width", diameter)
    .attr("height", diameter)
    .attr("class", "bubble");

    function catalog_clear(data) {
      // console.log(data);
      var children = [];
      if (data.children && data.children.length > 0) {
        for (var i = data.children.length - 1; i >= 0; i--) {
          children.push({name: data.children[i].name, size: data.children[i].size | getRandomInt(2, 20)});
        };
        return {name: data, children: children};
      };
    }

    function get_catalog_by_name(name, data, callback) {
      if (data.name == name) {
        callback(catalog_clear(data));
      } else if (data.children) {
        data.children.forEach(function(data) {
          get_catalog_by_name(name, data, callback);
        });
      }
    }
    var stack = [];
    function draw_bubble(name) {
      get_catalog_by_name(name, data, function(data) {
        if (!data) {
          return;
        };
        stack.push(name);
        svg.selectAll(".node").remove();
        var node = svg.selectAll(".node")
            .data(bubble.nodes((classes(data))))
          .enter()
          .append("g")
            .attr("class", "node")
            .attr("transform", function(d) { return "translate(" + d.x + "," + d.y + ")"; })
            .on("click", function(data) {
              draw_bubble(data.className);
            });

        node.append("circle")
            .style("fill", function(d, i) {
              if (d.depth == 0) {
                return "transparent";
              };
              return color(i);
            })
            .attr("r", 0)
            .transition()
            .duration(500)
            .delay(function(d) {
              return d.r * 7;
            })
            .attr("r", function(d) { return d.r; });

        node.append("text")
            .attr("dy", ".3em")
            .style("text-anchor", "middle")
            .style("fill", "white")
            .text(function(d) { 
                if (d.depth == 0) {
                    return "";
                }
                return d.className + "(" + d.value + ")"; })
            .style("font-size", 0)
            .transition()
            .duration(500)
            .delay(function(d) {
              return d.r * 7;
            })
            .style("font-size", function(d) {return d.r / 4;});
      });
    }
    draw_bubble("");
